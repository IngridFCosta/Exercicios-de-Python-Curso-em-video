#08- Escreva um programa que leia um valor em metros e o exiba convertido em centimetros e milimetros

valMetro=float(input("Escreva o valor em metros:"))
centimetro=valMetro*100
milimetro=valMetro*1000
print('Valor em metros:{}'.format(valMetro))
print('valor em centimetros: {}'.format(centimetro))
print('Valor em milimetros: {}'.format(milimetro))